-- AS 작업 목록 조회 
SELECT 
    W.NO        -- AS작업NO
    , W.AS_NO   -- AS요청NO
    , R.P_NO    AS PRODUCT_NO -- 상품NO
    , P.SERIAL_NUMBER  -- 상품시리얼번호
    , P.NAME    AS PRODUCT_NAME          -- 상품명
    , R.CUSTOMER_NAME
    , R.CUSTOMER_AREA
    , R.CUSTOMER_ADRESS 
    , R.CUSTOMER_PHONE 
    , R.PURCHASE_DATE 
    , R.WARRANTY_YN
    , R.ISSUE_TITLE
    , R.ISSUE_DESCRIPTION
    , R.PREFERRED_SERVICE_DATE
    , R.STATUS_CODE
    , S.NAME    AS STATUS_NAME 
    , W.FAULT_CODE
    , F.NAME    AS FAULT_NAME
    , W.EMP_NO          -- AS 담당자 사원번호
    , E.NAME    AS EMP_NAME 
    , TO_DATE (W.REPAIR_DATE, 'YYYY-MM-DD') AS REPAIR_DATE
    , W.REPAIR_DETALIS
    , W.ENROLL_DATE
    , W.MODIFY_DATE
    , W.DEL_YN
FROM AS_WORK W
JOIN AS_REQUEST R ON (W.AS_NO = R.NO)
JOIN PRODUCT_REGISTRATION P ON (R.P_NO = P.NO)
JOIN AS_STATUS S ON (R.STATUS_CODE = S.NO)
LEFT JOIN EMPLOYEE E ON (W.EMP_NO = E.NO)
LEFT JOIN FAULT_CODE F ON (W.FAULT_CODE = F.NO)
WHERE W.DEL_YN = 'N'
;


